"""
A model that contains only the arguments required for Basic Search. This will be used to determine
which search functions need to be executed.
"""

class BasicSearchModel(user_input, variants, number_results, order_by, search_method):
    def __init__():
        self.user_input = user_input # String
        self.variants = variants # String
        self.number_results = number_results # Integer
        self.search_method = search_method # Integer
        self.order_by = order_by # String